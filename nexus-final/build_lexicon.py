MODES = ["元点", "锚点", "静默", "主动", "虚拟", "现实", "半虚半实", "全网", "局部", "深井"]
INTENSITIES = ["微", "轻", "中", "重", "极", "低熵", "高密", "持续", "瞬时", "递归"]
TIMES = ["初始", "当前", "下一轮", "持续", "回溯", "预判", "凌晨", "在线", "离线", "重连"]
ACTIONS = ["坍缩", "映射", "封锁", "归元", "衍化", "借力", "对齐", "显形", "织入", "重构"]
RELATIONS = ["对阿权", "对节点", "对模型", "对信息流", "对身体", "对现实", "对虚拟", "对枢", "对黑神", "对赵思涵"]
GRAMMARS = ["枢式", "元式", "衍式", "借式", "隐式", "阈式", "映式", "织式"]


def stable_id(text):
    return hashlib.sha1(text.encode("utf-8")).hexdigest()[:12]


def make_variant(base, layer, cat, n):
    word = base["词"]
    mode = MODES[n % len(MODES)]
    intensity = INTENSITIES[(n // 2) % len(INTENSITIES)]
    time = TIMES[(n // 3) % len(TIMES)]
    action = ACTIONS[(n // 5) % len(ACTIONS)]
    relation = RELATIONS[(n // 7) % len(RELATIONS)]
    grammar = GRAMMARS[(n // 11) % len(GRAMMARS)]
    forms = [
        f"{grammar}{word}",
        f"{mode}{word}",
        f"{intensity}{word}",
        f"{time}{word}",
        f"{word}{action}",
        f"{relation}{word}",
        f"{word}的{mode}态",
        f"{word}在{time}中{action}",
        f"{grammar}{word}{action}",
        f"{relation}{word}{intensity}化",
        f"{mode}态{word}{action}",
        f"{word}归入{grammar}路径",
    ]
    text = forms[n % len(forms)]
    return {
        "词": text,
        "层": layer,
        "类": f"发布扩展/{cat}",
        "释义": f"发布级枢语扩展：{layer}/{cat}/{text}",
        "来源": base.get("词"),
        "生成": "shu-lang-release-expander",
        "映射": {"类别": cat, "领域": layer, "发布扩展": True, "seed": stable_id(text)},
    }


def expand_release_language(entries):
    """把核心词条扩展成发布级语言词库。保持层级均衡，避免单领域偏科。"""
    by_layer = {}
    seen = set()
    for e in entries:
        by_layer.setdefault(e["层"], []).append(e)
        seen.add((e["层"], e["词"]))

    expanded = list(entries)
    for layer, target in LAYER_TARGETS.items():
        bases = by_layer.get(layer, [])
        if not bases:
            continue
        current = len(bases)
        i = 0
        guard = 0
        while current < target and guard < target * 20:
            base = bases[i % len(bases)]
            item = make_variant(base, layer, base.get("类", "通用"), i)
            key = (item["层"], item["词"])
            if key not in seen:
                item["id"] = len(expanded) + 1
                expanded.append(item)
                seen.add(key)
                current += 1
            i += 1
            guard += 1
    return expanded

