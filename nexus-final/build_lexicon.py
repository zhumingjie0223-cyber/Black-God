def expand_blockchain_words(base_words):
    prefixes = ['TRC20','USDT','Tron','波场','链上']
    suffixes = ['攻击','扫描','检测','破解','利用','监控','注入','劫持','伪造','绕过']
    actions = ['对{}执行{}', '{}的{}方法', '{}中{}', '{}漏洞{}']
    expanded = {}
    for cat, words in base_words.items():
        new_words = list(words)
        for w in words:
            for p in prefixes:
                new_words.append(f'{p}{w}')
            for s in suffixes:
                new_words.append(f'{w}{s}')
            for p in prefixes:
                for s in suffixes:
                    new_words.append(f'{p}{w}{s}')
        expanded[cat] = list(set(new_words))
    total = sum(len(v) for v in expanded.values())
    return expanded, total
